list1 = [10, 5.5, [3,6], "Code",] #Mutable (data can be changed)
print(list1)

tuple1 = ("Hello", "Goodbye",) #Immutable (data cannot be changed)
print(tuple1)

dict1 = {"name":"Vedant", "age":20, "education":"Btech"} #Collection of key:value pair
print(dict1)

#everything in python is object oriented


#OUTPUT
'''
[10, 5.5, [3, 6], 'Code']
('Hello', 'Goodbye')
{'name': 'Vedant', 'age': 20, 'education': 'Btech'}
'''
