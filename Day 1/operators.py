#Arithmetic operators
'''
Addition (+)
Subtraction (-)
Multiplication (*)
Division (/)
Modulus (%)
Floor division (//)
Exponential (**)
'''

a = 20
b = 5

print("Addition of given number is:" , a + b)
print("Subtraction of given number is:" , a - b)
print("Multiplication of given number is:" , a * b)
print("Division of given number is:" , a / b)
print("Modulus of given number is:" , a % b)
print("Floor division of given number is:" , a // b)
print("Exponential of given number is:" , a ** b)


