#Typecasting: Conversion of one data type to another data type

# Implicit Typecasting (MANUAL)
a = "1"
b = "2"
print(a + b)
print(int(a) + int(b)) #string conveted to integer using int


# Explicit Typecasting (INTERPRETOR)
a = 5.5
b = 4
print(a + b) #int converted to float to carry out operation



#OUTPUT
'''
Implicit
12
3

Explicit
9.5
'''
